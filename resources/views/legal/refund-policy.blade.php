    <p>Эта страница описывает базовые принципы возврата средств внутри платформы Edusfera в случае отмены, переноса или несостоявшегося урока.</p>

    <h2>1. Когда возможен возврат</h2>
    <p>Возврат рассматривается, если оплаченный урок не состоялся, был отменён преподавателем, не был проведён в согласованное время либо платёж был списан ошибочно.</p>

    <h2>2. Как оформить возврат</h2>
    <p>Запрос на возврат инициируется через платформу или через поддержку Edusfera. Для ускорения обработки следует указать номер урока, дату и краткое описание ситуации.</p>

    <h2>3. Сроки рассмотрения</h2>
    <p>Платформа подтверждает получение обращения и проверяет статус урока, историю сделки и внутренние события по заказу. Срок рассмотрения зависит от полноты данных и статуса платежа.</p>

    <h2>4. Частичный и полный возврат</h2>
    <p>В зависимости от сценария может применяться полный возврат, частичный возврат или перенос на другой слот. Конкретное решение зависит от фактических обстоятельств урока.</p>

    <h2>5. Возврат при отмене до урока</h2>
    <p>Если урок отменён до начала и не был проведён, Edusfera оформляет возврат по внутренним правилам платформы и статусу операции.</p>

    <h2>6. Возврат при пакетной оплате</h2>
    <p>Для пакетов занятий платформа учитывает количество уже использованных уроков, оставшийся баланс и статус конкретного бронирования.</p>

    <h2>7. Поддержка</h2>
